weather = 'rainy'
case weather
    when 'sunny'
        puts "Wear sunscreen and sunglasses"
    when 'cloudy'
        puts "Wear a light sweater and keep an umbrella handy"
    when 'foggy'
        puts "Take a surgical mask with you"
    when 'rainy'
        puts "Wear rain coat and rain boots and take an umbrella with you"
end